begin
  if not exists (
    select 1 from information_schema.columns
    where table_schema = 'public' and table_name = 'feedback' and column_name = 'admin_reply'
  ) then
    alter table public.feedback add column admin_reply text;
  end if;

  if not exists (
    select 1 from information_schema.columns
    where table_schema = 'public' and table_name = 'feedback' and column_name = 'status'
  ) then
    alter table public.feedback
      add column status text not null default 'open'
      check (status in ('open', 'processing', 'done'));
  end if;

  if not exists (
    select 1 from information_schema.columns
    where table_schema = 'public' and table_name = 'feedback' and column_name = 'app_version'
  ) then
    alter table public.feedback add column app_version text;
  end if;

  if not exists (
    select 1 from information_schema.columns
    where table_schema = 'public' and table_name = 'feedback' and column_name = 'updated_at'
  ) then
    alter table public.feedback add column updated_at timestamptz not null default now();
  end if;
end
$$;

