-- =========================================================
-- 6. 管理員：新增／扣除玩家戒賭幣
-- =========================================================
create or replace function public.admin_adjust_coins_v134(
  admin_password text,
  target_user uuid,
  coin_delta integer,
  change_reason text
)
returns integer
language plpgsql
security definer
set search_path = public
as $$
declare
  old_balance integer;
  new_balance integer;
  actual_delta integer;
begin
  if admin_password <> '03070718' then
    raise exception '管理員密碼錯誤';
  end if;

  if target_user is null then
    raise exception '找不到玩家';
  end if;

  if coin_delta is null or coin_delta = 0 then
    raise exception '異動數量不可為 0';
  end if;

  insert into public.player_wallets (user_id, tokens)
  values (target_user, 10)
  on conflict (user_id) do nothing;

  select tokens into old_balance
  from public.player_wallets
  where user_id = target_user
  for update;

  new_balance := greatest(0, old_balance + coin_delta);
  actual_delta := new_balance - old_balance;

  update public.player_wallets
  set tokens = new_balance,
      updated_at = now()
  where user_id = target_user;

  insert into public.coin_transactions (
    user_id, amount, balance_after, reason, operator_type
  ) values (
    target_user,
    actual_delta,
    new_balance,
    coalesce(nullif(trim(change_reason), ''), '管理員調整'),
    'admin'
  );

  return new_balance;
end;
$$;

