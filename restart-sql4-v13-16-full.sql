-- =========================================================
-- 8. 管理員：回覆玩家意見
-- =========================================================
create or replace function public.admin_reply_feedback_v134(
  admin_password text,
  feedback_id text,
  reply_text text,
  new_status text
)
returns boolean
language plpgsql
security definer
set search_path = public
as $$
declare
  safe_status text;
begin
  if admin_password <> '03070718' then
    raise exception '管理員密碼錯誤';
  end if;

  safe_status := coalesce(nullif(new_status, ''), 'processing');

  if safe_status not in ('open', 'processing', 'done') then
    raise exception '無效的處理狀態';
  end if;

  update public.feedback
  set admin_reply = reply_text,
      status = safe_status,
      updated_at = now()
  where id::text = feedback_id;

  return found;
end;
$$;

