-- =========================================================
-- 4. 一般玩家建立／取得錢包
-- =========================================================
create or replace function public.ensure_wallet_v134()
returns public.player_wallets
language plpgsql
security definer
set search_path = public
as $$
declare
  uid uuid := auth.uid();
  w public.player_wallets;
begin
  if uid is null then
    raise exception '請先登入';
  end if;

  insert into public.player_wallets (user_id, tokens)
  values (uid, 10)
  on conflict (user_id) do nothing;

  select * into w
  from public.player_wallets
  where user_id = uid;

  if not exists (
    select 1 from public.coin_transactions where user_id = uid
  ) then
    insert into public.coin_transactions (
      user_id, amount, balance_after, reason, operator_type
    ) values (
      uid, 10, w.tokens, '一般玩家新手禮物', 'system'
    );
  end if;

  return w;
end;
$$;

