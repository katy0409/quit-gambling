-- =========================================================
-- 10. 商城真正扣除戒賭幣
-- =========================================================
create or replace function public.spend_coins_v135(
  spend_amount integer,
  spend_reason text default '商城購買'
)
returns table (tokens integer)
language plpgsql
security definer
set search_path = public
as $$
declare
  uid uuid := auth.uid();
  current_tokens integer;
begin
  if uid is null then
    raise exception '尚未登入';
  end if;

  if spend_amount is null or spend_amount <= 0 then
    raise exception '扣款金額錯誤';
  end if;

  perform public.ensure_wallet_v134();

  select w.tokens into current_tokens
  from public.player_wallets w
  where w.user_id = uid
  for update;

  if current_tokens < spend_amount then
    raise exception '戒賭幣不足';
  end if;

  current_tokens := current_tokens - spend_amount;

  update public.player_wallets
  set tokens = current_tokens,
      updated_at = now()
  where user_id = uid;

  insert into public.coin_transactions (
    user_id, amount, balance_after, reason, operator_type
  ) values (
    uid,
    -spend_amount,
    current_tokens,
    coalesce(nullif(trim(spend_reason), ''), '商城購買'),
    'player'
  );

  return query select current_tokens;
end;
$$;

-- =========================================================
-- 11. 領取每日任務獎勵
-- =========================================================
create or replace function public.claim_daily_task_v135(
  task_key text,
  reward_amount integer
)
returns table (tokens integer)
language plpgsql
security definer
set search_path = public
as $$
declare
  uid uuid := auth.uid();
  current_tokens integer;
begin
  if uid is null then
    raise exception '尚未登入';
  end if;

  if task_key is null or trim(task_key) = '' then
    raise exception '任務代碼錯誤';
  end if;

  if task_key not in ('login', 'checkin', 'journal', 'square', 'avatar') then
    raise exception '無效的每日任務';
  end if;

  if reward_amount not between 1 and 10 then
    raise exception '獎勵金額錯誤';
  end if;

  perform public.ensure_wallet_v134();

  insert into public.daily_task_claims (
    user_id, task_date, task_key, reward
  ) values (
    uid, current_date, task_key, reward_amount
  );

  update public.player_wallets
  set tokens = player_wallets.tokens + reward_amount,
      updated_at = now()
  where user_id = uid
  returning player_wallets.tokens into current_tokens;

  insert into public.coin_transactions (
    user_id, amount, balance_after, reason, operator_type
  ) values (
    uid,
    reward_amount,
    current_tokens,
    '每日任務：' || task_key,
    'system'
  );

  return query select current_tokens;

exception
  when unique_violation then
    raise exception '今天已領取此任務';
end;
$$;

-- =========================================================
-- 12. 管理員：完整重置指定玩家
-- 前端呼叫名稱與參數：
-- admin_full_reset_user_v1314(admin_password, reset_reason, target_user)
-- =========================================================
drop function if exists public.admin_full_reset_player_v1314(text, uuid, text);
drop function if exists public.admin_full_reset_user_v1314(text, text, uuid);
create or replace function public.admin_full_reset_user_v1314(
  admin_password text,
  reset_reason text,
  target_user uuid
)
returns boolean
language plpgsql
security definer
set search_path = public, auth
as $$
declare
  safe_reason text;
  reset_time timestamptz := now();
begin
  if admin_password <> '03070718' then
    raise exception '管理員密碼錯誤';
  end if;

  if target_user is null then
    raise exception '找不到玩家';
  end if;

  if not exists (
    select 1 from auth.users where id = target_user
  ) then
    raise exception '玩家帳號不存在';
  end if;

  safe_reason := nullif(trim(reset_reason), '');

  if safe_reason is null then
    raise exception '請填寫完整重置原因';
  end if;

  -- 先刪除有外鍵依賴的資料
  delete from public.transactions where user_id = target_user;
  delete from public.saving_goals where user_id = target_user;
  delete from public.debts where user_id = target_user;
  delete from public.assets where user_id = target_user;

  -- 其他玩家資料
  delete from public.categories where user_id = target_user;
  delete from public.diaries where user_id = target_user;
  delete from public.checkins where user_id = target_user;
  delete from public.relapses where user_id = target_user;
  delete from public.photos where user_id = target_user;
  delete from public.finance_reports where user_id = target_user;
  delete from public.excel_import_logs where user_id = target_user;
  delete from public.daily_task_claims where user_id = target_user;
  delete from public.feedback where user_id = target_user;
  delete from public.square_messages where user_id = target_user;

  -- 清除舊戒賭幣紀錄
  delete from public.coin_transactions where user_id = target_user;

  -- 錢包恢復 10 枚
  insert into public.player_wallets (user_id, tokens, updated_at)
  values (target_user, 10, reset_time)
  on conflict (user_id)
  do update set
    tokens = excluded.tokens,
    updated_at = excluded.updated_at;

  -- 重建設定，並留下前端清除本機角色／衣櫃／商城資料所需的標記
  delete from public.settings where user_id = target_user;

  insert into public.settings (
    user_id,
    home_title,
    home_subtitle,
    theme_color,
    salary_amount,
    line_url,
    extra,
    created_at,
    updated_at
  ) values (
    target_user,
    '重新開始',
    '今天不賭，因為我值得更好的生活。',
    '#f7f3ed',
    0,
    null,
    jsonb_build_object(
      'admin_full_reset_at', reset_time,
      'admin_full_reset_reason', safe_reason,
      'admin_full_reset_version', 'v13.16',
      'admin_full_reset_requested', true
    ),
    reset_time,
    reset_time
  );

  -- 保留管理員完整重置紀錄
  insert into public.coin_transactions (
    user_id,
    amount,
    balance_after,
    reason,
    operator_type,
    created_at
  ) values (
    target_user,
    10,
    10,
    '管理員完整重置：' || safe_reason,
    'admin',
    reset_time
  );

  -- 只保留 Auth 登入帳號；清除玩家暱稱與頭像
  update public.profiles
  set display_name = null,
      avatar_url = null,
      updated_at = reset_time
  where id = target_user;

  return true;
end;
$$;


